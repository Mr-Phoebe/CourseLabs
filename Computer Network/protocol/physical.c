int phl_sq_len_2 ()
{
	return (c_socket_buf_up - c_socket_buf_low + SOCKET_BUF_SIZE) % SOCKET_BUF_SIZE;
}

int phl_sq_len ()
{
	return phl_sq_len_2 () / 2;
}

void send_byte (char data)
{
	is_phl_ready = 1;

	// One byte - send it
	if (c_phl_capacity && c_socket_buf_low == c_socket_buf_up)
	{
		send (socket_mine, &data, 1, 0);

		// Able to send less bytes
		--c_phl_capacity;
	}

	// More than One byte
	else
	{
		// Unable to buffer too much byte
		if (phl_sq_len_2 () == SOCKET_BUF_SIZE - 1)
		{
			lprintf (str_Newline);
			log_printf (str_FatalAbort,
						"Physical Layer Sending Queue overflow");
			exit (0);
		}

		// Buffer data
		socket_send_buf[c_socket_buf_up] = data;
		c_socket_buf_up = (c_socket_buf_up + 1) % SOCKET_BUF_SIZE;
	}
}

void send_frame (unsigned char *frame, int size)
{
	// Send Beg_Frame
	send_byte (-1);

	for (unsigned int i = 0; i < size; ++i)
	{
		// Send Low-4-bit
		send_byte (frame[i] & 0xF);

		// Send High-4-bit
		send_byte (frame[i] >> 4);
	}

	// Send End_Frame
	send_byte (-1);
}

int recv_frame (unsigned char *buffer, int size)
{
	// No buffered frame
	if (!p_socket_recved)
	{
		lprintf (str_Newline);
		log_printf (str_FatalAbort,
					"recv_frame(): Receiving Queue is empty");
		exit (0);
	}

	unsigned int c_recved_buf_size = p_socket_recved->size;

	// buffer size is too small
	if (size < p_socket_recved->size)
	{
		char str_tmp[512];

		sprintf (str_tmp,
				 "recv_frame(): %d-byte buffer is too small"
				 " to save %d-byte received frame",
				 size, p_socket_recved->size);

		lprintf (str_Newline);
		log_printf (str_FatalAbort, str_tmp);
		exit (0);
	}

	// Copy buffer
	memcpy (buffer, p_socket_recved->data, c_recved_buf_size);

	// Is there Next buf to read
	Socket_Recv *p_socket_recved_nxt = p_socket_recved->next_ptr;
	if (!p_socket_recved_nxt)
		p_socket_recved_prev = NULL;

	// Free Curr buf
	free (p_socket_recved);

	// Goto Next buf
	p_socket_recved = p_socket_recved_nxt;

	// Return buffer size
	return c_recved_buf_size;
}

